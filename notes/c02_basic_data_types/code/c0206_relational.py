"""
Relational Operations

    equals  (==)
    not equals(!=)
    less than   (<)
    less than or equals (<=)
    greater than    (>)
    greater than or equals  (>=)

Relational operation always returns either True or False

"""


# %% equals  ( == )
print(5 == 6)          # False
print(4+1 == 6-1)      # True


# %% not equals( != )
print(5 != 6)          # True
print(4+1 != 6-1)      # False

# %% less than   ( < )

print(4 < 5)        # True
print(4 < 4)        # False
print(5 < 4)        # False

# %% less than or equals ( <= )
print(4 <= 5)        # True
print(4 <= 4)        # True
print(5 <= 4)        # False


# %% greater than    ( > )
print(4 > 5)        # False
print(4 > 4)        # False
print(5 > 4)        # True

# %% greater than or equals  ( >= )
print(4 >= 5)        # False
print(4 >= 4)        # True
print(5 >= 4)        # True
print('A' > 'B')     # False

